'use strict';

export class Motorcycle {
    constructor(key, year, make, model, km, color, price, description, sold, images, videos) {
        this.key = key;
        this.year = year;
        this.make = make;
        this.model = model;
        this.km = km;
        this.color = color;
        this.price = price;
        this.description = description;
        this.sold = sold;
        this.images = images;
        this.videos = videos;
    }
}